`timescale 1ns / 1ps

//=============================================================
// Testbench: top_module_tb
//=============================================================
module top_module_tb;

    //---------------------------------------------------------
    // Component Declaration (DUT)
    //---------------------------------------------------------
    reg clk = 0;
    reg rst_n = 0;
    reg rx_in = 1;
    reg [3:0] force_state = 4'b0;
    reg test_mode = 0;
    wire tx_out;
    wire clk_out;
    wire [3:0] state_dbg;
    
    reg [255:0] decoded;
    
    //---------------------------------------------------------
    // Instantiate UUT
    //---------------------------------------------------------
    top_module uut (
        .clk(clk),
        .rst_n(rst_n),
        .rx_in(rx_in),
        .force_state(force_state),
        .test_mode(test_mode),
        .tx_out(tx_out),
        .clk_out(clk_out),
        .state_dbg(state_dbg)
    );
    
     
     
    //---------------------------------------------------------
    // Clock generation
    //---------------------------------------------------------
    localparam real CLK_PERIOD = 73.7; // ns => 13.56 MHz
    always #(CLK_PERIOD/2) clk = ~clk;

    //---------------------------------------------------------
    // Timing parameters (for 212 kbps when bitrate_sel="01")
    //---------------------------------------------------------
    integer tb_clks = 128;
    integer tx_clks = 64;
    integer t1_clks = 32;
    
    `include "tb_tasks.v"  // <-- Include tasks and CRC function


/*        
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
                                                                  Stimulus
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
*/

    initial begin
        $dumpfile("wave.vcd");
        $dumpvars(0,top_module_tb);
        // Release reset
        #(2000 * CLK_PERIOD);
        rst_n = 1;    
        #(1236 * CLK_PERIOD);

 
/*        
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
                                                                                                     Normal Mode
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
*/
        
/*        
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
                                                                  IDEL STATE
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
*/


//--------------------------------------------------- Stimulus process force state from idel --------------------------------------------------

        $display("******************************** Stimulus process force state from IDEL ********************************");       
	run_force_state(4'b0);
//                        ^
//                    IDEL_STATE

//---------------------------------------------------------------------------------------------------------------------------------------------          
	#(80000 * CLK_PERIOD);
//------------------------------------------------------- Stimulus process Random frame -------------------------------------------------------

	$display("******************************** Stimulus process Random frame (IDEL) ********************************"); 
	generate_50_random_frames();

//--------------------------------------------------------------------------------------------------------------------------------------------- 
	#(80000 * CLK_PERIOD);
//------------------------------------------------------ Stimulus process 10 WEPA Command -----------------------------------------------------

	$display("******************************** Stimulus process 10 WEPA Command ********************************"); 
	send_short_frame(7'h10);
	
//--------------------------------------------------------------------------------------------------------------------------------------------- 
	#(80000 * CLK_PERIOD);
	
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/*        
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
                                                                  READY1 STATE
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
*/

//--------------------------------------------------- Stimulus process force state from READI1 --------------------------------------------------

        $display("******************************** Stimulus process force state from READY1 ********************************"); 
	run_force_state(4'b1);
//                        ^
//                    READY1_STATE

//---------------------------------------------------------------------------------------------------------------------------------------------   
	#(80000 * CLK_PERIOD);
//------------------------------------------------------- Stimulus process Random frame -------------------------------------------------------

	$display("******************************** Stimulus process Random frame (READY1) ********************************"); 
	generate_50_random_frames();

//--------------------------------------------------------------------------------------------------------------------------------------------- 
	#(80000 * CLK_PERIOD);
//------------------------------------------------ Stimulus process ANTI_COLIISION CL1 Command ------------------------------------------------

        $display("******************************** Stimulus process ANTI_COLIISION CL1 Command ********************************"); 
        generate_frames_param_0_to_255(8'h20, 40'h8833221188);
	
//--------------------------------------------------------------------------------------------------------------------------------------------- 
	#(80000 * CLK_PERIOD);
//---------------------------------------------------- Stimulus process SELECT CL1 Command ----------------------------------------------------

	$display("******************************** Stimulus process SELECT CL1 Command ********************************");
	send_standard_frame_manual_parity(72, 72'h82AD88332211889320, 9'b101111110);
//                                         ^            ^                  ^
//                                       size         frame             parity 
	
//--------------------------------------------------------------------------------------------------------------------------------------------- 
	#(80000 * CLK_PERIOD);
	
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/*        
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
                                                                  READY2 STATE
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
*/

//--------------------------------------------------- Stimulus process force state from READI2 --------------------------------------------------

        $display("******************************** Stimulus process force state from READY2 ********************************");
	run_force_state(4'b0010);
//                         ^
//                    READY2_STATE

//---------------------------------------------------------------------------------------------------------------------------------------------   
	#(80000 * CLK_PERIOD);
//------------------------------------------------------- Stimulus process Random frame -------------------------------------------------------

	$display("******************************** Stimulus process Random frame (READY2) ********************************");
	generate_50_random_frames();

//--------------------------------------------------------------------------------------------------------------------------------------------- 
	#(80000 * CLK_PERIOD);
//------------------------------------------------ Stimulus process ANTI_COLIISION CL2 Command ------------------------------------------------

	$display("******************************** Stimulus process ANTI_COLIISION CL2 Command ********************************"); 
        generate_frames_param_0_to_255(8'h70, 40'h0077665544);
	
//--------------------------------------------------------------------------------------------------------------------------------------------- 
	#(80000 * CLK_PERIOD);
//---------------------------------------------------- Stimulus process SELECT CL2 Command ----------------------------------------------------

	$display("******************************** Stimulus process SELECT CL2 Command ********************************");
	send_standard_frame_auto_crc_parity(56, 56'h00776655449370);
//                                          ^          ^              
//                                        size       frame             
	
//--------------------------------------------------------------------------------------------------------------------------------------------- 
	#(80000 * CLK_PERIOD);
	
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/*        
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
                                                                  ACTIVE STATE
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
*/

//--------------------------------------------------- Stimulus process force state from ACTIVE --------------------------------------------------

        $display("******************************** Stimulus process force state from ACTIVE ********************************");
	run_force_state(4'b0011);
//                         ^
//                    ACTIVE_STATE

//---------------------------------------------------------------------------------------------------------------------------------------------   
	#(80000 * CLK_PERIOD);
//------------------------------------------------------- Stimulus process Random frame -------------------------------------------------------

	$display("******************************** Stimulus process Random frame (ACTIVE) ********************************");
	generate_50_random_frames();

//--------------------------------------------------------------------------------------------------------------------------------------------- 
	#(80000 * CLK_PERIOD);
//--------------------------------------------------------Stimulus process WRITE Command--------------------------------------------------------       

        $display("******************************** Stimulus process WRITE Command ********************************"); 
	send_custom_frame_auto_crc_parity(
	    8'h66,       // first byte
	    0,           // second byte start
	    255,         // second byte end
	    32,          // remaining data bits
	    32'h11111111 // remaining data value
	);

//--------------------------------------------------------------------------------------------------------------------------------------------- 
	#(80000 * CLK_PERIOD);
//-------------------------------------------------------Stimulus process READ Command---------------------------------------------------------      
        
        $display("******************************** Stimulus process READ Command ********************************"); 
	generate_frames_param_0_to_255(8'h77, 0);	
	
//--------------------------------------------------------------------------------------------------------------------------------------------- 
	#(80000 * CLK_PERIOD);
//------------------------------------------------------ Stimulus process 10 WEPA Command -----------------------------------------------------

	$display("******************************** Stimulus process HALT Command ********************************"); 
	send_standard_frame_manual_parity(32, 32'h3FA60044, 4'b1111);
	
//--------------------------------------------------------------------------------------------------------------------------------------------- 
	#(80000 * CLK_PERIOD);

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/*        
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
                                                                  HALT STATE
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
*/

//--------------------------------------------------- Stimulus process force state from HALT --------------------------------------------------

        $display("******************************** Stimulus process force state from HALT ********************************");       
	run_force_state(4'b0100);
//                         ^
//                     HALT_STATE

//---------------------------------------------------------------------------------------------------------------------------------------------          
	#(80000 * CLK_PERIOD);
//------------------------------------------------------- Stimulus process Random frame -------------------------------------------------------

	$display("******************************** Stimulus process Random frame (HALT) ********************************"); 
	generate_50_random_frames();

//--------------------------------------------------------------------------------------------------------------------------------------------- 
	#(80000 * CLK_PERIOD);
//------------------------------------------------------ Stimulus process 10 WEPA Command -----------------------------------------------------

	$display("******************************** Stimulus process 10 WEPA Command ********************************"); 
	send_short_frame(7'h10);
	
//--------------------------------------------------------------------------------------------------------------------------------------------- 
	#(80000 * CLK_PERIOD);

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	rst_n = 0; 
	#(2000 * CLK_PERIOD);
        rst_n = 1;    
        #(1236 * CLK_PERIOD);

/*        
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
                                                                                                     Test Mode
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
*/

	test_mode = 1;
	#(80000 * CLK_PERIOD);
//---------------------------------------------------Stimulus process Random frame (Test Mode) ------------------------------------------------ 

	$display("******************************** Stimulus process Random frame (Test Mode) ********************************"); 
	generate_50_random_frames();
	
//--------------------------------------------------------------------------------------------------------------------------------------------- 
	#(80000 * CLK_PERIOD);
//------------------------------------------------Stimulus process WRITE Command (Test Mode) --------------------------------------------------  
	
	$display("******************************** Stimulus process WRITE Command (Test Mode) ********************************"); 
	send_custom_frame_auto_crc_parity(
	    8'hB1,       // first byte
	    0,           // second byte start
	    255,         // second byte end
	    32,          // remaining data bits
	    32'h22222222 // remaining data value
	);
	
//--------------------------------------------------------------------------------------------------------------------------------------------- 
	#(80000 * CLK_PERIOD);
//-------------------------------------------------Stimulus process READ Command (Test Mode) -------------------------------------------------- 

	$display("******************************** Stimulus process READ Command (Test Mode) ********************************"); 	
	generate_frames_param_0_to_255(8'hB2, 0);
	
//--------------------------------------------------------------------------------------------------------------------------------------------- 	
	#(80000 * CLK_PERIOD);

	$display("******************************** Stimulus process forcing Command (Test Mode) ********************************"); 	
	run_test_mode_force_state();

//--------------------------------------------------------------------------------------------------------------------------------------------- 
	#(80000 * CLK_PERIOD);
	
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        //-----------------------------------------------------
        // Simulation end
        //-----------------------------------------------------
        $display("Simulation completed");
        #(8000 * CLK_PERIOD);
        $finish;
    end
endmodule
